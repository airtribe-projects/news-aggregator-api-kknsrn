// Authentication middleware
const jwt = require('jsonwebtoken');
const { ERROR_MESSAGES } = require('../../config/constants');

exports.protect = async (req, res, next) => {
    let token;

    // Check for token in headers
    if (req.headers.authorization && req.headers.authorization.startsWith('Bearer')) {
        token = req.headers.authorization.split(' ')[1];
    }

    // Make sure token exists
    if (!token) {
        return res.status(401).json({ success: false, message: ERROR_MESSAGES.UNAUTHORIZED });
    }

    try {
        // Verify token
        const decoded = jwt.verify(token, process.env.JWT_SECRET);
        req.user = decoded;
        next();
    } catch (error) {
        if (error.name === 'TokenExpiredError') {
            return res.status(401).json({ success: false, message: ERROR_MESSAGES.TOKEN_EXPIRED });
        }
        return res.status(401).json({ success: false, message: ERROR_MESSAGES.INVALID_TOKEN });
    }
};

exports.authorize = (...roles) => {
    return (req, res, next) => {
        if (!roles.includes(req.user.role)) {
            return res.status(403).json({ success: false, message: 'Not authorized to access this route' });
        }
        next();
    };
};
